import math
print(math.sqrt(16))  # istenilen değerin karekökünü bulur
print(math.exp(2))  # e (Euler sabiti) sayısının istenilen kuvvetinin alınmasını sağlar.
print(math.log(2,2))  # logaritma hesaplar
print(math.cos(45))  # değerin kosinüsünü alır
print(pow(2,2))  # pow(x,y) fonksiyonu x sayısının y. kuvvetinin alınmasını sağlar.
print(math.degrees(45))   # radyandan dereceye çevirmeye yarar
print(math.radians(45))   # dereceden radyana çevirmeye yarar
print(math.fabs(-5))   #fabs(x) fonksiyonu x değerinin mutlak değerinin alınması işlemini gerçekleştirir.